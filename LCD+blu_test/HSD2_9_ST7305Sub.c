/*====================================================*/
  
void Write_Register(unsigned int idat)
{
  unsigned int m,da=idat;
  unsigned  int j;
     
  IOCLR = RS;
  IOCLR = nCSB; 	
   	
   for(j=0;j<8;j++)
   {   	
     m=da;
     IOCLR = SCLK;
     
     if((m&0x80)==0x80)    
        {        
          IOSET = SDIN;          
        }        
     else
        {         
          IOCLR = SDIN;
        }
          
          da=da<<1;      
	      IOSET = SCLK;	
	      IOCLR = SCLK;     
        }        

     IOSET = nCSB;
}


